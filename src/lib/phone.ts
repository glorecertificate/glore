export const splitPhoneNumber = (value?: string) => {
  if (!value) return { country: undefined, national: '' }

  const codes = Object.entries(COUNTRY_CALLING_CODES).sort(([, a], [, b]) => b.length - a.length)
  for (const [country, code] of codes) {
    if (value.startsWith(code)) return { country, national: value.slice(code.length).trim() }
  }

  return { country: undefined, national: value }
}

export const formatPhoneNumber = (country: string | undefined, national: string) => {
  const trimmed = national.trim()
  if (!trimmed) return ''
  const code = country ? COUNTRY_CALLING_CODES[country] : undefined
  return code ? `${code} ${trimmed}` : trimmed
}
